<script>
    let untukPilihPemesanan = function() {
        $(document).on('change', '#kd_pemesanan', function() {
            var ini = $(this);

            $.ajax({
                type: "post",
                url: "<?= admin_url() ?>pembayaran/get_pemesanan",
                dataType: 'json',
                data: {
                    id: ini.val(),
                },
                success: function(response) {
                    $('#total').val(response.total);
                    $('#metode_pembayaran').val(response.metode_pembayaran);
                }
            });
        });
    }();

    // untuk tambah & ubah data
    var untukTambahDanUbahData = function() {
        $(document).on('submit', '#form-add-upd', function(e) {
            e.preventDefault();
            $('#kd_pemesanan').attr('required', 'required');
            $('#bayar').attr('required', 'required');

            if ($('#form-add-upd').parsley().isValid() == true) {
                $.ajax({
                    method: $(this).attr('method'),
                    url: $(this).attr('action'),
                    data: new FormData(this),
                    contentType: false,
                    processData: false,
                    cache: false,
                    dataType: 'json',
                    beforeSend: function() {
                        $('#save').attr('disabled', 'disabled');
                        $('#save').html('<i class="fa fa-spinner"></i>&nbsp;Menunggu...');
                    },
                    success: function(response) {
                        swal({
                            title: response.title,
                            text: response.text,
                            icon: response.type,
                            button: response.button,
                        }).then((value) => {
                            location.reload();
                        });
                    }
                })
            }
        });
    }();
</script>